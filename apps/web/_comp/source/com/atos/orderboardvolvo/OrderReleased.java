package com.atos.orderboardvolvo;

public class OrderReleased {

	private String shopOrder;
	private String quantity;
	private String firstSFC;
	private String lastSFC;
	private String material;
	private String routing;
	private String operation;
	private String processLoss;
	
	public String getShopOrder() {
		return shopOrder;
	}
	public void setShopOrder(String shopOrder) {
		this.shopOrder = shopOrder;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getFirstSFC() {
		return firstSFC;
	}
	public void setFirstSFC(String firstSFC) {
		this.firstSFC = firstSFC;
	}
	public String getLastSFC() {
		return lastSFC;
	}
	public void setLastSFC(String lastSFC) {
		this.lastSFC = lastSFC;
	}
	public String getMaterial() {
		return material;
	}
	public void setMaterial(String material) {
		this.material = material;
	}
	public String getRouting() {
		return routing;
	}
	public void setRouting(String routing) {
		this.routing = routing;
	}
	public String getOperation() {
		return operation;
	}
	public void setOperation(String operation) {
		this.operation = operation;
	}
	public String getProcessLoss() {
		return processLoss;
	}
	public void setProcessLoss(String processLoss) {
		this.processLoss = processLoss;
	}
	
}
